            <h3>Ponencia #1: "El ABC del Bitcoin"</h3>
            <div class="conference-media">
                <img src="/mvc_sds/src/9.jpeg" class="conference-image img-right">
                <p class="image-caption">Una representación visual de la tecnología Bitcoin.</p>
            </div>
            <p>Entonces, ¿qué decidimos nosotros? ¿Entender cómo funciona el dinero o realmente pasarle a esa persona? Créanme que los sistemas políticos del mundo prefieren personas obedientes. Personas que hacen de regla. Yo no estoy acá para decirte sí, todo está bien. Estoy hablando de un tema de Bitcoin que necesito que te haga revolución en la mente. Si seas una persona crítica, dice, pa, ¿por qué lo vas a usar? La gente me dice, Miguel, enséñame el ritmo, enséñame cómo hacer un café. Yo no les enseño nada. Este tema no es para sentarte y estafarte. No es para salir en TikTok diciendo, mándenme dinero. Es un tema que realmente requiere tiempo, chicos, porque tiene que ver con economía.</p>
            <p>Louis Momice, que es uno de los pioneros ahí, dijo que el dinero es el bien más comercializable de las personas. Que las personas adquieren porque quieren ofrecerles gastos posteriores de intercambio personal, haciendo referencia que el valor del dinero no solamente tiene que ver con gastarlo ya, sino que se analiza y se piensa posteriormente, a futuro. Y es lo que la mayoría de las personas no piensan. Por qué les hablo de esto?Porque el tema de Bitcoin no solamente nace porque a alguien se le ocurrió hacer un tipo de tecnología. Aparte, ustedes saben que la palabra tecnología, ¿verdad? En el griego, technet o logistudio, tiene que ver no con lo que entendemos ahorita de los sistemas, sino que tiene que ver con la técnica, con el arte. Con todo eso nace la palabra tecnología.</p>
            <p>Bitcoin no nació como, hey, alguien se le ocurrió, despertó y voy a crear una tecnología ahorita que sea una moneda digital, sino que lleva una filosofía detrás. Y esa filosofía parte de algo. Una economía quebrada en el mundo, no desde hace poco, sino que si usted se pone a estudiar esa economía quebrada, viene desde mucho antes incluso que los años 80. Si la Reserva Federal fue creada en 1913, formalmente, desde ahí se comienza a desquebrar muchas cosas en el mundo, económicamente hablando. Entonces, la economía austriaca de economía y solamente quiero compartir algo contigo así a grosor modo para que entendamos más o menos de qué se trata. Porque todo eso tiene una consecuencia. O sea, si hablamos de economía, tiene una situación. Y solamente lo voy a leer acá, solo para hacerlo. Vamos a ver algunos puntos claves de la economía que inicialmente casi en todos los países se están usando.</p>
            <p>Ahora yo le pregunto a usted, que ya sé que tiene mucho conocimiento aquí en la universidad. Si usted guarda 10 mil dólares ahorita en 2025, ¿cuánto creen que va a valer los 10 mil dólares en 2030? Si obviamente el dinero va perdiendo valor todo el tiempo. Entonces, guardar dólares, y esto lo vamos a lo mejor a dar en el tema de tecnología, guardar dólares debajo del colchón es un problema.</p>
            <p>Ha sido una evolución la sal, un tiempo, fue de mucho valor. De hecho, ahí viene la palabra salario que nosotros usamos, salario mínimo. Roma le pagaba a sus funcionarios con sal, porque la sal era muy escasa. Casi nadie tenía acceso a las playas, lo que es la sociedad sobre Roma. Ponía gente esclava a trabajar, pero luego se hizo bastante común la sal, y hoy nos damos el humo de tirarla, de botarla, no hay ningún problema. Pero un tiempo, fue algo de valor, un dinero muy importante.</p>
