            <h3>Ponencia #2: "Inteligencia Artificial generativa, de la idea a la acción"</h3>
            <div class="conference-media">
                <img src="/mvc_sds/src/ia.jpeg" class="conference-image img-right">
                <p class="image-caption">La IA, una herramienta que nos impulsa a ser más eficientes.</p>
            </div>
            <p>Entonces ahí es donde en realidad determinan que independientemente de la regla que nos estemos formando exactamente, la inteligencia artificial sí tiene un impacto. A partir de esto entonces decimos, la guía no está reemplazando, nos está ayudando a ser más eficientes en nuestro trabajo.  Si nosotros no sabemos cómo utilizarlas, en qué función utilizarlas, pero decimos, ¿qué es la inteligencia artificial? Sí, Algoritmos, ¿qué más? Datos, modelos, exactamente, ¿qué más? En este sentido, si nos vamos a la RAE, en la definición que tenía en 2021 decía que es la capacidad de entender, comprender y es la capacidad de resolver problemas. Entender y la capacidad de comunicarnos y resolver problemas. Y aquí hay un debate álgido entre los ingenieros, entre los filósofos y entre los científicos de si llamar inteligencia artificial es en realidad un nombre comercial o tiene que ver con la practicalidad de esto.
                Porque dicen los filósofos que es antropomorfizar. Repitan conmigo, antropomorfizar. Pregunto, ¿qué es antropomorfizar? Antropomorfizar es adjudicar características humanas a un objeto, a una tecnología.</p>
            <p> Nadie imaginaba que iban a venir a asimilar o a enaltar el pensamiento humano. La parte de la cognición, ese proceso de pensar y de simular el pensamiento humano. Cuando nosotros vemos, decimos, la capacidad de conocer, la capacidad de comprender, la capacidad de tener datos, información y conocimiento, no creíamos, nadie se reesperaba que lo iban a ser los charlos.
                Porque todos pensábamos que iban a ser robots humanoides, que iban a tomar ese tipo de decisiones. Ahí tenemos la hipótesis. En cripto funciona la hipótesis, para poder comprar y vender cuando usted tenga ascenso y descenso.
                Y entonces también funciona en la parte de inteligencia artificial, por supuesto. Pero viene y reemplaza, o cuando menos simula, la capacidad de pensar. Pero repito, nosotros como seres biológicos, como inteligencia de carbono, tenemos la capacidad de entender, conocer, comprender y aprender.
                La inteligencia artificial solo tiene información. Que si vamos a competir con ella, pues no debería. Como seres humanos tenemos la capacidad, si somos públicos, de estudiar entre 130 y 742 libros en nuestra vida.
                La inteligencia artificial, si vemos los patentes con los que ha sido entrenada, es la capacidad de haber aprendido sobre 10 millones de libros. Por cada libro que leemos, nosotros solo retenemos el 10%. La inteligencia artificial almacena el 100% de la información de cada libro.</p>
